import java.util.*;

public class Pattern {
    public static void main(String[] args) {
        int n = 8;
        int i;
        for (i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println();
        }
        
    }

}
